logo=$(cat <<'EOF'
 __    __            _______. _______ .___________. __    __  .______   
|  |  |  |          /       ||   ____||           ||  |  |  | |   _  \  
|  |  |  |  ______ |   (----`|  |__   `---|  |----`|  |  |  | |  |_)  | 
|  |  |  | |______| \   \    |   __|      |  |     |  |  |  | |   ___/  
|  `--'  |      .----)   |   |  |____     |  |     |  `--'  | |  |      
 \______/       |_______/    |_______|    |__|      \______/  | _|      
                                                                        
EOF
)

colors=(
  '\033[38;5;93m'  
  '\033[38;5;129m' 
)

IFS=$'\n' read -rd '' -a lines <<<"$logo"
for i in "${!lines[@]}"; do
    color_index=$((i % ${#colors[@]}))
    echo -e "${colors[color_index]}${lines[i]}"
done